// Data to flow we use logical expressions such as if and  else
let myNumber = 1;

if (myNumber > 10) {
    console.log('My number is greater than 10');
} else {
    console.log('My number is less than 10');
}

const weatherToday = "rainy";

if (weatherToday == "cloudy") {
    console.log('Perhaps bring a jacket');
} else {
    console.log('You\'ll be fine with a Tshirt');
}